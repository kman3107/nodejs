
// Binary operators - two values operators
// Addition
console.log(5 + 4);
// Subtraction
console.log(9 - 4);
// Multiplication
console.log(3 * 4);
// Division
console.log(7 / 2);
// Mod operator - gives the remainder of division
console.log(7 % 2);
console.log(12.3 % 5);

// Uniary operators - one value operators
let value = 2;
value++;
console.log(value);
++value;
console.log(value);
value--;
console.log(value);
--value;
console.log(value);
